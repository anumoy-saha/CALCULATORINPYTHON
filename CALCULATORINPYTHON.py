print("Enter the first number")
num1=int(input())
print("Enter the second number")
num2=int(input())
print("Enter the operator")
opt=input()

if opt=='+':
		num4=num1+num2
		print(num4)
		
elif opt=='*':
			num5=num1*num2
			print(num5)
elif opt=='/':
				num6=num1/num2
				print(num6)
elif opt=='-':
				num7=num1-num2
				print(num7)